class ImprimeMeses {

	public static void main (String[] args){
	    
		int mes = 0;
		while (mes <= 12){
			switch (mes){
				case 1 :
					System.out.println("Janeiro");
					break;
				case 2 :
					System.out.println("Fevereiro");
					break;
				default  : 
					//kkkkkkk
					break;
			}
		mes++;
		}
	}
}
